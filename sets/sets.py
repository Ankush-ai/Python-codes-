# Using union(), intersection(), and difference() for sets
set1 = {1, 2, 3}
set2 = {3, 4, 5}
union_set = set1.union(set2)  # Returns the union of set1 and set2
intersection_set = set1.intersection(set2)  # Returns the intersection of set1 and set2
difference_set = set1.difference(set2)  # Returns the elements in set1 but not in set2
print(set1,set2)
print(intersection_set)
print(union_set)